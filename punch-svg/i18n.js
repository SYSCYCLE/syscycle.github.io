// ============================================
// i18n Engine
// ============================================

let currentLang = 'tr';

function detectBrowserLanguage() {
	const saved = localStorage.getItem('svg-puncher-lang');
	if (saved && I18N_TRANSLATIONS[saved]) return saved;
	const browserLang = (navigator.language || navigator.userLanguage || 'en').substring(0, 2).toLowerCase();
	if (I18N_TRANSLATIONS[browserLang]) return browserLang;
	return 'en';
}

function t(key) {
	const lang = I18N_TRANSLATIONS[currentLang] || I18N_TRANSLATIONS['en'];
	return lang[key] || I18N_TRANSLATIONS['en'][key] || key;
}

function applyTranslations() {
	document.documentElement.lang = currentLang;

	document.querySelectorAll('[data-i18n]').forEach(el => {
		const key = el.getAttribute('data-i18n');
		const translation = t(key);
		if (translation) {
			if (translation.includes('<')) {
				el.innerHTML = translation;
			} else {
				el.textContent = translation;
			}
		}
	});

	document.querySelectorAll('[data-i18n-title]').forEach(el => {
		const key = el.getAttribute('data-i18n-title');
		const translation = t(key);
		if (translation) el.setAttribute('title', translation);
	});

	const selector = document.getElementById('language-selector');
	if (selector) selector.value = currentLang;

	updateCanvasMetaIndicators();

	if (typeof baseShape === 'undefined' || baseShape === null) {
		const statusTip = document.getElementById('canvas-status-tip');
		if (statusTip && activeInteractionMode === 'instant') {
			statusTip.innerHTML = `<span class="text-blue-400">${t('statusStep1')}</span> ${t('statusStep1Text')}`;
		}
	}
}

function changeLanguage(lang) {
	if (!I18N_TRANSLATIONS[lang]) return;
	currentLang = lang;
	localStorage.setItem('svg-puncher-lang', lang);
	applyTranslations();

	if (typeof setInteractionMode === 'function' && typeof activeInteractionMode !== 'undefined') {
		const helpText = document.getElementById('mode-help-text');
		if (helpText) {
			if (activeInteractionMode === 'instant') {
				helpText.innerHTML = `<strong class="text-blue-400">${t('instantCarveLabel')}</strong> ${t('instantCarveHelp')}`;
			} else {
				helpText.innerHTML = `<strong class="text-blue-400">${t('manualSelectLabel')}</strong> ${t('manualSelectHelp')}`;
			}
		}
	}

	if (typeof updateLayersTree === 'function') {
		updateLayersTree();
	}

	if (typeof updateUISelectionSlots === 'function') {
		updateUISelectionSlots();
	}
}

document.addEventListener('DOMContentLoaded', function() {
	currentLang = detectBrowserLanguage();
	applyTranslations();
});
